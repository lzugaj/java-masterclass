package com.luv2code.introduction.variables;

public class VariablesMain {

    public static void main(String[] args) {
        int firstNumber = 5; // variable declaration
        System.out.println("First number value is = " + firstNumber);

        int secondNumber =  (5 + 10) - ((2 * 3) / 3);
        System.out.println("Second number value is = " + secondNumber);

        int thirdNumber = 6;
        int fourthNumber = secondNumber * 2;
        int fifthNumber = thirdNumber + fourthNumber;
        System.out.println("Fifth number value is = " + fifthNumber);
    }
}
